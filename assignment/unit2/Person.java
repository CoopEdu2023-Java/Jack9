package assignment.unit2;

public class Person {
    // 实例变量
    private String name;
    private int age;
    private String country;

    // 无参数构造函数
    public Person() {
    }

    public Person(String name, int age, String country) {
        this.name = name;
        this.age = age;
        this.country = country;
    }

}
